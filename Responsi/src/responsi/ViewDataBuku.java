    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
         String str1 = fJudul.getText();
        String str2 = fPenulis.getText();
        String str3 = fRating.getText();
        String str4 = fHarga.getText();
        
        if(str1.isEmpty()||str2.isEmpty()||str3.isEmpty()||str4.isEmpty()){
        JOptionPane.showMessageDialog(this, "Harap isi seluruh Field","Coba Lagi",JOptionPane.ERROR_MESSAGE);
        
        }else{
            controller.setJudul(str1);
            controller.setPenulis(str2);
            controller.setRating(parseDouble(str3));
            controller.olahData(parseDouble(str4));
            controller.updateData(tabelBuku.getValueAt(tabelBuku.getSelectedRow(), 0).toString());
            lihatData();
            fJudul.setText("");
            fPenulis.setText("");
            fRating.setText("");
            fHarga.setText("");
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
         try {
    int jawab;
    
    if ((jawab = JOptionPane.showConfirmDialog(null, "Ingin menghapus data?", "konfirmasi", JOptionPane.YES_NO_OPTION)) == 0) {
      st = cn.createStatement();
      st.executeUpdate("DELETE FROM buku WHERE judul='"
          + tabModel.getValueAt(tabelBuku.getSelectedRow(), 0) + "'");
      lihatData();
      fJudul.setText("");
        fPenulis.setText("");
        fRating.setText("");
        fHarga.setText("");
    }
  } catch (Exception e) {
    e.printStackTrace();
  }
    }//GEN-LAST:event_btnHapusActionPerformed

