    public void lihatData(){
     try {
    st = cn.createStatement();
    tabModel.getDataVector().removeAllElements();
    tabModel.fireTableDataChanged();
    rs = st.executeQuery("SELECT * FROM buku ");
    
    while (rs.next()) {
      Object[] data = {
        rs.getString("judul"),
        rs.getString("penulis"),
        rs.getString("rating"),
        rs.getString("harga"),
      };
        
        tabModel.addRow(data);
    }
  } catch(Exception e) {
    e.printStackTrace();
  }
    
    }

